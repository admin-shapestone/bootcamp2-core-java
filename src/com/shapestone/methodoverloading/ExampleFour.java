package com.shapestone.methodoverloading;

public class ExampleFour {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		System.out.println(a + b); 
		System.out.println(a + "Nitish");
		System.out.println("Nitish" + "Chintu" + 2 + 2 + "Rayapati");
		System.out.println(2 + 2 + "Rayapati" + "Nitish" + "Chintu");
	}

}
