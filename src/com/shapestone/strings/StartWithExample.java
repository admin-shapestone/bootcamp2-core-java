package com.shapestone.strings;

public class StartWithExample {

	public static void main(String[] args) {
		String s1 = "Devansh";
		String s2 = "devansh";
		String s3 = "DEVANSH";
		System.out.println(s1.startsWith("De"));//using starting letters output :true
		System.out.println(s2.startsWith("De"));//
				
	}

}
